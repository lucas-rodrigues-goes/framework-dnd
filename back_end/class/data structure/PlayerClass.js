"use strict";
try {

    var PlayerClass = class {

        //=====================================================================================================
        // Class parameters
        //=====================================================================================================

        #name
        #type
        #subclasses
        #featureChoices
        #proficiencies
        #description

        
        //=====================================================================================================
        // Getter methods
        //=====================================================================================================

        get name() { return this.#name; }
        


        //=====================================================================================================
        // Instance management
        //=====================================================================================================

        constructor({
            name = "", // String
        }) {

            // Instancing
            this.#name = name

        }

        object() {
            return {
                name: this.#name,
            };
        }

        //=====================================================================================================
    }


} catch (e) {
    MapTool.chat.broadcast("" + e + "\n" + e.stack);
}
